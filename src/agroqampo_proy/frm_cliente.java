    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        Connection cn=newConnection.getConnection();
        try{
            JasperReport jr = (JasperReport) JRLoader.loadObject(frm_cliente.class.getResource("/Reportes/cliente2.jasper"));
            Map parametros = new HashMap<>();
            parametros.put("Titulo", "Reporte cliente");

            JasperPrint jp = JasperFillManager.fillReport(jr, parametros, cn);
            JasperViewer jv = new JasperViewer(jp, false);
            jv.setVisible(true);
        }
        catch (JRException ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
        
        JOptionPane.showMessageDialog(null, "Haz impreso un reporte");

    }//GEN-LAST:event_jButton3ActionPerformed

